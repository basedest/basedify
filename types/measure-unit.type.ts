export const enum MeasureUnit {
    Times = 'times', // if not specified, it's assumed to be times
    Repetitions = 'reps', // same as times, but more relevant in certain contexts

    Minutes = 'min',
    Hours = 'h',
    Days = 'd',

    Kilograms = 'kg',
    Grams = 'g',

    Liters = 'l',
    Milliliters = 'ml',
}
